package org.example.locators;


public class LocatorsPlaywright {

    /*
    Quick Guide - Краткое руководство.
    These are the recommended built in locators. - Это рекомендуемые встроенные локаторы.
    Page.getByRole(); //для поиска по явным и неявным атрибутам доступности.
    Page.getByText(""); //для поиска по текстовому содержимому.
    Page.getByLabel(); //, чтобы найти элемент управления формой по тексту связанной метки.
    Page.getByPlaceholder(); //, чтобы найти ввод по заполнителю.
    Page.getByAltText(); //для поиска элемента, обычно изображения, по его текстовой альтернативе.
    Page.getByTitle(); //, чтобы найти элемент по его атрибуту заголовка.
    Page.getByTestId(); //для поиска элемента на основе его data-testidатрибута (могут быть настроены другие атрибуты).
    */
}
